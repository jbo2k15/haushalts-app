import ical from 'node-ical'
import prisma from '../lib/prisma.js'

const WASTE_TYPES = [
  { match: 'papier', title: 'Papiertonne rausstellen' },
  { match: 'restmüll', title: 'Restmülltonne rausstellen' },
  // "Restabfall" ist bei manchen Entsorgern (z.B. EDG Dortmund) der offizielle
  // Begriff für den Restmüll und taucht so im iCal-Feed auf, statt "Restmüll".
  // Gleicher Titel wie restmüll, damit es dieselbe Tonne bleibt.
  { match: 'restabfall', title: 'Restmülltonne rausstellen' },
  { match: 'gelbe tonne', title: 'Gelbe Tonne rausstellen' },
  // "Wertstofftonne" ist in manchen Kommunen der offizielle Begriff für die
  // gelbe Tonne und taucht so im iCal-Feed auf, statt "Gelbe Tonne".
  { match: 'wertstoff', title: 'Gelbe Tonne rausstellen' },
]

// Exported separately so the matching logic can be unit-tested without
// mocking the full iCal fetch.
export function matchWasteType(summary) {
  const lower = summary.toLowerCase()
  return WASTE_TYPES.find(w => lower.includes(w.match)) || null
}

export function toDateString(date) {
  const d = new Date(date)
  const year = d.getFullYear()
  const month = String(d.getMonth() + 1).padStart(2, '0')
  const day = String(d.getDate()).padStart(2, '0')
  return `${year}-${month}-${day}`
}

export function subtractOneDay(dateStr) {
  const [y, m, day] = dateStr.split('-').map(Number)
  const d = new Date(Date.UTC(y, m - 1, day - 1))
  return toDateString(d)
}

function todayBerlin() {
  return new Intl.DateTimeFormat('sv-SE', { timeZone: 'Europe/Berlin' }).format(new Date())
}

export async function syncWasteCalendar() {
  const url = process.env.WASTE_ICAL_URL
  if (!url) return
  if (!url.startsWith('https://')) {
    console.error('Abfallkalender: WASTE_ICAL_URL muss mit https:// beginnen')
    return
  }

  try {
    const events = await Promise.race([
      ical.async.fromURL(url),
      new Promise((_, reject) => setTimeout(() => reject(new Error('iCal fetch timeout')), 15000)),
    ])

    const today = todayBerlin()

    // Find each waste type's NEXT upcoming collection in the feed (dueDate =
    // day before collection). Only the nearest one per type is tracked —
    // the feed often lists many months ahead, and only the next occurrence
    // is actionable.
    const nearestByTitle = new Map() // title -> dueDate

    for (const event of Object.values(events)) {
      if (event.type !== 'VEVENT') continue
      const summary = typeof event.summary === 'object' ? event.summary?.val : event.summary || ''
      const wasteType = matchWasteType(summary)
      if (!wasteType) continue

      const collectionDate = toDateString(event.start)
      if (collectionDate < today) continue // collection already happened

      const dueDate = subtractOneDay(collectionDate)
      const current = nearestByTitle.get(wasteType.title)
      if (!current || dueDate < current) nearestByTitle.set(wasteType.title, dueDate)
    }

    // Only actually materialize a task once it's due (the day before
    // collection, or later if a sync run was missed) — not weeks in advance.
    const upcoming = [...nearestByTitle.entries()]
      .filter(([, dueDate]) => dueDate <= today)
      .map(([title, dueDate]) => ({ title, dueDate }))

    // Remove legacy daily-type auto-generated tasks (old format without dueDate)
    await prisma.task.deleteMany({ where: { isAutoGenerated: true, type: 'daily' } })

    // Load existing auto-generated tasks
    const existing = await prisma.task.findMany({ where: { isAutoGenerated: true } })
    const existingByKey = new Map(existing.map(t => [`${t.title}-${t.dueDate}`, t]))

    // Upsert: create missing, reactivate deactivated
    let created = 0
    let updated = 0
    for (const { title, dueDate } of upcoming) {
      const key = `${title}-${dueDate}`
      const match = existingByKey.get(key)
      if (match) {
        if (!match.isActive) {
          await prisma.task.update({ where: { id: match.id }, data: { isActive: true } })
          updated++
        }
        existingByKey.delete(key) // mark as seen
      } else {
        await prisma.task.create({
          data: {
            title,
            type: 'once',
            priority: 'normal',
            dueDate,
            isAutoGenerated: true,
            isActive: true,
          },
        })
        created++
        existingByKey.delete(key)
      }
    }

    // Anything auto-generated left over that wasn't matched above:
    // - past-due ones are handled by the nightly expireOnce/cleanup jobs
    // - future-due ones that were created too early (before this fix, or no
    //   longer the nearest occurrence) get removed — unless already
    //   completed, in which case leave the early completion alone
    const remaining = [...existingByKey.values()]
    const premature = remaining.filter(t => t.dueDate && t.dueDate > today)
    if (premature.length > 0) {
      const completions = await prisma.taskCompletion.findMany({
        where: { taskId: { in: premature.map(t => t.id) } },
        select: { taskId: true },
      })
      const completedIds = new Set(completions.map(c => c.taskId))
      const toRemove = premature.filter(t => !completedIds.has(t.id))
      if (toRemove.length > 0) {
        await prisma.task.deleteMany({ where: { id: { in: toRemove.map(t => t.id) } } })
      }
    }

    console.log(`Abfallkalender synchronisiert: ${upcoming.length} Termine (${created} neu, ${updated} reaktiviert)`)
  } catch (err) {
    console.error('Fehler beim Synchronisieren des Abfallkalenders:', err.message)
  }
}
