import ical from 'node-ical'
import prisma from '../lib/prisma.js'

const WASTE_TYPES = [
  { match: 'papier', title: 'Papiertonne rausstellen' },
  { match: 'restmüll', title: 'Restmülltonne rausstellen' },
  { match: 'gelbe tonne', title: 'Gelbe Tonne rausstellen' },
]

function toDateString(date) {
  const d = new Date(date)
  const year = d.getFullYear()
  const month = String(d.getMonth() + 1).padStart(2, '0')
  const day = String(d.getDate()).padStart(2, '0')
  return `${year}-${month}-${day}`
}

function subtractOneDay(dateStr) {
  const d = new Date(dateStr)
  d.setDate(d.getDate() - 1)
  return toDateString(d)
}

export async function syncWasteCalendar() {
  const url = process.env.WASTE_ICAL_URL
  if (!url) return
  if (!url.startsWith('https://')) {
    console.error('Abfallkalender: WASTE_ICAL_URL muss mit https:// beginnen')
    return
  }

  try {
    const events = await Promise.race([
      ical.async.fromURL(url),
      new Promise((_, reject) => setTimeout(() => reject(new Error('iCal fetch timeout')), 15000)),
    ])

    const seen = new Set()
    const newTasks = []
    let sortOrder = 10000

    for (const event of Object.values(events)) {
      if (event.type !== 'VEVENT') continue
      const summary = (typeof event.summary === 'object' ? event.summary?.val : event.summary || '').toLowerCase()
      const wasteType = WASTE_TYPES.find(w => summary.includes(w.match))
      if (!wasteType) continue

      const collectionDate = toDateString(event.start)
      const taskDate = subtractOneDay(collectionDate)
      const weekday = new Date(taskDate).getDay()
      const key = `${wasteType.title}-${weekday}`
      if (seen.has(key)) continue
      seen.add(key)

      newTasks.push({
        title: wasteType.title,
        type: 'daily',
        priority: 'normal',
        weekdays: JSON.stringify([weekday]),
        isAutoGenerated: true,
        isActive: true,
        sortOrder: sortOrder++,
      })
    }

    // Upsert: keep existing tasks (and their completions) by matching title+weekday
    const existing = await prisma.task.findMany({ where: { isAutoGenerated: true } })

    for (const task of newTasks) {
      const weekday = JSON.parse(task.weekdays)[0]
      const match = existing.find(e => e.title === task.title && JSON.parse(e.weekdays || '[]')[0] === weekday)
      if (match) {
        await prisma.task.update({ where: { id: match.id }, data: { isActive: true, sortOrder: task.sortOrder } })
      } else {
        await prisma.task.create({ data: task })
      }
    }

    // Remove tasks no longer in the calendar
    const newKeys = new Set(newTasks.map(t => `${t.title}-${JSON.parse(t.weekdays)[0]}`))
    const toDelete = existing.filter(e => {
      const weekday = JSON.parse(e.weekdays || '[]')[0]
      return !newKeys.has(`${e.title}-${weekday}`)
    })
    if (toDelete.length > 0) {
      await prisma.task.deleteMany({ where: { id: { in: toDelete.map(t => t.id) } } })
    }

    console.log(`Abfallkalender synchronisiert: ${seen.size} Einträge`)
  } catch (err) {
    console.error('Fehler beim Synchronisieren des Abfallkalenders:', err.message)
  }
}
